package com.example.erik.contentresolvercontactsdagger;

public class ContactData {
    private String name;

    public ContactData(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return this.name;
    }
}
